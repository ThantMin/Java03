package java03;

public class sample {
   

    public static void main(String args[]) {
        int j = 9;
        int p = 0;
        //Looping & Conditional Statement
        do {
            for (int i = 5; i <= j; i++) {
                if (i == 6) {
                    System.out.println("3 left");

                } else if (i == 9) {
                    System.out.println("End");

                } else {
                    System.out.println(i);
                    
                }

            }
            break;
        } while (p == 0);
        
        
    }
    }
    

